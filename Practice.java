class Pratice{
    public static void main(String[] args) {
      
        int a = 5;

        System.out.println("Post Increment: " + a++); // 5
        System.out.println("After Post Increment: " + a); // 6

        int b = 5;
        System.out.println("Pre Increment: " + ++b); // 6
        System.out.println("After Pre Increment: " + b); // 6
    }
}